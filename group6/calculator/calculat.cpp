#include <iostream>
using namespace std;

int main() {
    double num1 = 7.5;   // First number
    double num2 = 4.0;   // Second number

    double result = num1 * num2;

    cout << "Number 1: " << num1 << endl;
    cout << "Number 2: " << num2 << endl;
    cout << "Multiplication result: " << result << endl;

    return 0;
}